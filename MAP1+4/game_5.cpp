/*------------------------------------------------------------------------------
   グローバル変数の定義
------------------------------------------------------------------------------*/
unsigned char g_BGMNo5 = 0;


/*------------------------------------------------------------------------------
   初期化関数
------------------------------------------------------------------------------*/
void InitGame5(void)
{
	InitPlayer();
	InitEnemy5();
	InitMap5();
	InitTimer();

	g_BGMNo5 = LoadSound("data/BGM/BGM.wav");
	PlaySound(g_BGMNo5, 255);
}

/*------------------------------------------------------------------------------
   終了処理をする関数
------------------------------------------------------------------------------*/
void UninitGame5()
{
	StopSoundAll();

	//初期化とは逆順に終了処理を行う
	UninitMap5();
	UninitPlayer();
	UninitEnemy5();
	UninitTimer();
}

/*------------------------------------------------------------------------------
   更新処理をする関数
------------------------------------------------------------------------------*/
void UpdateGame5(void)
{
	UpdateMap5();
	UpdateEnemy5();
	UpdatePlayer();

	UpdateCollision();
	UpdateTimer();

	//スペースキーが押されていて、フェード処理中ではないとき
	if (Keyboard_IsKeyDown(KK_ENTER) ||
		IsButtonTriggered(0, XINPUT_GAMEPAD_B)
		&& GetFadeState() == FADE_NONE)
	{
		//RESULTへ移行する
		SceneTransition(SCENE_RESULT);
	}
}

/*------------------------------------------------------------------------------
   描画処理をする関数
------------------------------------------------------------------------------*/
void DrawGame5(void)
{
	DrawMap5();
	DrawPlayer();
	DrawEnemy5();
	DrawTimer();
}